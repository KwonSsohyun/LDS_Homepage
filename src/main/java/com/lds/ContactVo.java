package com.lds;

public class ContactVo {
    private int custIdx;
    private String custDate;
    private String custName;
    private String custEmail;
    private String custHp;
    private String custQuestion;

    public int getCustIdx() {
        return custIdx;
    }

    public void setCustIdx(int custIdx) {
        this.custIdx = custIdx;
    }

    public String getCustDate() {
        return custDate;
    }

    public void setCustDate(String custDate) {
        this.custDate = custDate;
    }

    public String getCustName() {
        return custName;
    }

    public void setCustName(String custName) {
        this.custName = custName;
    }

    public String getCustEmail() {
        return custEmail;
    }

    public void setCustEmail(String custEmail) {
        this.custEmail = custEmail;
    }

    public String getCustHp() {
        return custHp;
    }

    public void setCustHp(String custHp) {
        this.custHp = custHp;
    }

    public String getCustQuestion() {
        return custQuestion;
    }

    public void setCustQuestion(String custQuestion) {
        this.custQuestion = custQuestion;
    }
}

